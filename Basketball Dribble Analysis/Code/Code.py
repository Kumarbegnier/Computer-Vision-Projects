    "# Function for dribble detection\n",
    "def detect_dribble(frame):\n",
    "    # Convert frame to grayscale\n",
    "    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)\n",
    "    \n",
    "    # Apply Gaussian blur\n",
    "    blurred = cv2.GaussianBlur(gray, (5, 5), 0)\n",
    "    \n",
    "    # Apply Canny edge detection\n",
    "    edges = cv2.Canny(blurred, 50, 150)\n",
    "    \n",
    "    # Apply Hough transform to detect lines\n",
    "    lines = cv2.HoughLinesP(edges, 1, np.pi/180, 100, minLineLength=50, maxLineGap=10)\n",
    "    \n",
    "    # Initialize dribble count\n",
    "    dribble_count = 0\n",
    "    \n",
    "    # Check if lines are detected\n",
    "    if lines is not None:\n",
    "        # Loop through lines\n",
    "        for line in lines:\n",
    "            x1, y1, x2, y2 = line[0]\n",
    "            \n",
    "            # Calculate the distance between the line and the center of the frame\n",
    "            distance = np.sqrt((x1 + (x2 - x1)/2 - frame.shape[1]/2)**2 + (y1 + (y2 - y1)/2 - frame.shape[0]/2)**2)\n",
    "            \n",
    "            # If the distance is less than a certain threshold, it's a dribble\n",
    "            if distance < 100:\n",
    "                dribble_count += 1\n",
    "    \n",
    "    return dribble_count\n",
    "\n",
    "# Function for speed calculation\n",
    "def calculate_speed(frame, dribble_count, start_time):\n",
    "    # Calculate time since last frame\n",
    "    time_since_last_frame = time.time() - start_time\n",
    "    \n",
    "    # Calculate number of dribbles per second\n",
    "    dribbles_per_second = dribble_count / time_since_last_frame\n",
    "    \n",
    "    # Calculate ball speed\n",
    "    ball_speed = dribbles_per_second * 0.01\n",
    "    \n",
    "    return ball_speed\n"
